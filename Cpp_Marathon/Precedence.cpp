/*
* Write a program to calculate the size (in bytes) of various data 
* types (int, char, float, double) using the sizeof operator.
*/
#include<iostream>
using namespace std;

int main()
{
    cout<<"The size of int: "<<sizeof(int)<<endl;
    cout<<"The sizeof char: "<<sizeof(char)<<endl;
    cout<<"The size of float: "<<sizeof(float)<<endl;
    cout<<"The size of double: "<<sizeof(double)<<endl;
    return 0;
}